# Example variables of different types
integer_var = 42
float_var = 3.14
string_var = "Hello, Python!"
list_var = [1, 2, 3]
dictionary_var = {"name": "Alice", "age": 30}

# Check the data type using type()
print(type(integer_var))       # Output: <class 'int'>
print(type(float_var))         # Output: <class 'float'>
print(type(string_var))        # Output: <class 'str'>
print(type(list_var))          # Output: <class 'list'>
print(type(dictionary_var))    # Output: <class 'dict'>
